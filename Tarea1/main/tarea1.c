typedef struct {
    float real;
    float imag;
} complejo;

void calcularFFT(float *entrada_amplitud_tiempo, int longitud, float frecuencia_muestreo, complejo *salida_fft) {
    for (int k = 0; k < longitud; k++) {
        salida_fft[k].real = 0;
        salida_fft[k].imag = 0;

        for (int n = 0; n < longitud; n++) {
            float angulo = 2 * M_PI * k * n / longitud;
            float cos_angulo = cos(angulo);
            float sin_angulo = -sin(angulo);

            salida_fft[k].real += entrada_amplitud_tiempo[n] * cos_angulo;
            salida_fft[k].imag += entrada_amplitud_tiempo[n] * sin_angulo;
        }
    }
    for (int i = 0; i < longitud; i++) {
        salida_fft[i].real /= longitud;
        salida_fft[i].imag /= longitud;
    }
}

int calcularMin(float *peaks) {
    int min = 0;
    for(int i=1; i<5; i++) {
        if (peaks[i]<peaks[min]) min = i;
    }
    return min;
}

float calcularRMS(float arreglo[], int longitud) {
    float sumaCuadrados = 0.0;

    // Calcula la suma de los cuadrados de los elementos del arreglo
    for (int i = 0; i < longitud; i++) {
        sumaCuadrados += arreglo[i] * arreglo[i];
    }

    // Calcula la media de los cuadrados
    float mediaCuadrados = sumaCuadrados / longitud;

    // Calcula la raíz cuadrada de la media de los cuadrados para obtener el RMS
    float rms = sqrt(mediaCuadrados);

    return rms;
}

